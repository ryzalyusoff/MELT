public class Student {
    private String Stu_ID;
    private String Stu_Firstname;
    private String Stu_Surname;
    private String Stu_Passcode;

    /**
     * @return the Stu_ID
     */
    public String getStu_ID() {
        return Stu_ID;
    }

    /**
     * @param Stu_ID the Stu_ID to set
     */
    public void setStu_ID(String Stu_ID) {
        this.Stu_ID = Stu_ID;
    }

    /**
     * @return the Stu_Firstname
     */
    public String getStu_Firstname() {
        return Stu_Firstname;
    }

    /**
     * @param Stu_Firstname the Stu_Firstname to set
     */
    public void setStu_Firstname(String Stu_Firstname) {
        this.Stu_Firstname = Stu_Firstname;
    }

    /**
     * @return the Stu_Surname
     */
    public String getStu_Surname() {
        return Stu_Surname;
    }

    /**
     * @param Stu_Surname the Stu_Surname to set
     */
    public void setStu_Surname(String Stu_Surname) {
        this.Stu_Surname = Stu_Surname;
    }

    /**
     * @return the Stu_Passcode
     */
    public String getStu_Passcode() {
        return Stu_Passcode;
    }

    /**
     * @param Stu_Passcode the Stu_Passcode to set
     */
    public void setStu_Passcode(String Stu_Passcode) {
        this.Stu_Passcode = Stu_Passcode;
    }

    
    
}
